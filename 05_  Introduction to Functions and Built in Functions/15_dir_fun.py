# 15. Practice - Using the built-in dir() Function


# Prints all the names in the current local scope
print(dir())

# Prints the value of the __name__ variable
print(__name__)

# Prints the value of the __builtins__ module
print(__builtins__)

# Prints all the names of the attributes and methods in the __builtins__ module
print(dir(__builtins__))


