# 116. Practice - Working with Prefix Unary Operators

x = 5

print(+x)   # same as 5
print(-x)   # negates to -5
print(~x)   # Bitwise NOT,flips bits: ~5 = -6


my_name = 'Ujjwal'
print("My name is  :",my_name)
print(not my_name)  # not empty name 
print(not not my_name)   #get bool
print(bool(my_name))