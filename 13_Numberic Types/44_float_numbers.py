# 44. Float Numbers

# Examples
avg_price = 17.23
print(avg_price)

print(type(avg_price))

# convert float to int 
price = int(avg_price)
print(price)   # 17
print(type(price))


# str to float conversion 
str_temp = '28.5'
print(type(str_temp))
temp = float(str_temp)
print(temp)
print(type(temp))


# Rounding of numbers : rounding the nearest integers 

avg_price = 30.45
print(round(avg_price))   # 30


# more examples 
rate =  0.89    # 1
print(round(rate))