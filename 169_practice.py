# 169. Practice - Iterating through Lists and Tuples using For-In Loops

countries = ['India','USA','UK','Canada']

for country in countries:
    print(f"Welcome to the {country}")

months = ('Jan','Feb','Apr','May','June')
for month in months:
    print(f"Current month is {month}")
    
fruits = ['Apple', 'Banana', 'Mango', 'Pineapple']
for fruit in fruits:
    print(f"I like {fruit}")
